public class Variables {

    public static void main(String[] args) {
        byte edad=35; // Declaración e iniciación en la misma línea
        System.out.println(edad);

        /*
        la siguiente instrucción imprime en la consola (System.out) el valor
        de la variable edad
        */
        edad=75;
        System.out.println(edad);
    }
}
